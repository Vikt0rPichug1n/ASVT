; Лабораторная работа №3, Вариант 13
; Поиск номера строки с минимальной суммой в матрице 3x4
; Все вычисления выполняет сопроцессор (FPU)
; Две подпрограммы: SumRow и CheckMin

.686
.model flat, stdcall
.stack 4096

ExitProcess PROTO STDCALL :DWORD

.data
    Matrix  DW   15,  -23,   8,   42   ; Строка 0: сумма = 42
            DW  -10,    5,  -7,   18   ; Строка 1: сумма = 6
            DW    0,   -3,  12,  -30   ; Строка 2: сумма = -21 (мин.)

    N       EQU  3                     ; Количество строк
    M       EQU  4                     ; Количество столбцов

    BigNum  REAL4  999999.0            ; Начальный "очень большой" минимум
    MinRow  DW     ?                   ; Номер строки с минимальной суммой

.code

; =================================================================
; Подпрограмма 1: SumRow
; Назначение: считает сумму элементов в одной строке матрицы
; Вход:
;   ESI — адрес первого элемента строки
;   ECX — количество элементов в строке (M)
; Выход:
;   ST(0) — сумма элементов строки (вещественное число)
;   ESI — адрес после последнего элемента строки (для продолжения)
; Использует: FPU (стек), флаги
; =================================================================
SumRow PROC
    fldz                               ; ST(0) = 0.0 — начальная сумма (Floating-point LoaD Zero)

SumLoop:
    fild  word ptr [esi]               ; Загрузить целый элемент в ST(0) (Floating-point Integer LoaD)
    faddp st(1), st(0)                 ; ST(1) = ST(1) + ST(0), вытолкнуть (Floating-point ADD and Pop)
    add  esi, 2                        ; Перейти к следующему элементу (2 байта)
    loop SumLoop                       ; Повторить M раз

    ret                                ; Вернуться из подпрограммы
SumRow ENDP

; =================================================================
; Подпрограмма 2: CheckMin
; Назначение: сравнивает сумму строки с текущим минимумом
;             и обновляет минимум + номер строки, если нужно
; Вход:
;   ST(0) — сумма текущей строки
;   ST(1) — текущий минимум
;   BX    — номер текущей строки
;   MinRow — адрес переменной для номера строки (глобальная)
; Выход:
;   ST(0) — удалён (остаётся только текущий минимум в ST(0))
;   MinRow — обновлён, если сумма меньше минимума
;   MinSum — обновлён в FPU (ST(1) до выхода, потом станет ST(0))
; Использует: FPU, флаги
; =================================================================
CheckMin PROC
    fcomi st(0), st(1)                 ; Сравнить сумму с минимумом (Floating-point COMpare Integer)
    jae  NotLess                       ; Если ST(0) >= ST(1) — не обновлять --- Jump if Above or Equal (перейти, если выше или равно)

    fst  st(1)                         ; Обновить минимум: ST(1) = ST(0) (Floating-point STore)
    mov  MinRow, bx                    ; Сохранить номер строки

NotLess:
    fstp  st(0)                        ; Удалить сумму строки из стека (Floating-point STore and Pop)
    ret                                ; Вернуться из подпрограммы
CheckMin ENDP

; =================================================================
; Главная программа
; =================================================================
Start:
    ; --- Инициализация ---
    lea  esi, Matrix                   ; ESI = адрес начала матрицы
    mov  edx, N                        ; EDX = счётчик строк (внешний цикл)
    xor  ebx, ebx                      ; EBX = 0 (номер текущей строки)

    finit                              ; Инициализация сопроцессора (Floating-point INITialize) 
    fld  BigNum                        ; ST(0) = 999999.0 (текущий минимум) (Floating-point LoaD)

    ; --- Главный цикл по строкам ---
MainLoop:
    ; Подготовка к вызову SumRow: адрес уже в ESI, счётчик — в ECX
    mov  ecx, M                        ; ECX = количество элементов в строке
    call SumRow                        ; Вызов подпрограммы 1
    ; Результат: ST(0) = сумма, ST(1) = текущий минимум

    ; Вызов подпрограммы сравнения и обновления минимума
    call CheckMin                      ; Вызов подпрограммы 2
    ; Результат: ST(0) = текущий минимум, MinRow обновлён (если нужно)

    inc  bx                            ; Переход к следующему номеру строки
    dec  edx                           ; Уменьшаем счётчик строк
    jnz  MainLoop                      ; Если EDX не 0 — повторяем ---Jump if Not Zero (перейти, если не ноль)

    ; --- Завершение ---
    finit                              ; Очистка FPU

    Invoke ExitProcess, 0
END Start