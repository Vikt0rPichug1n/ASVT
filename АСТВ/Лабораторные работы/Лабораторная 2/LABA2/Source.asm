; Лабораторная работа №2, Вариант 13
; Цель: Обработка данных X, Y, Z.
; X = 16E6h, Y = D45Bh, Z = C9C9h

.686
.model flat, stdcall
.stack 4096

; Прототипы функций Windows API
ExitProcess PROTO STDCALL :DWORD

.data
    ; Исходные данные (расположены один за другим)
    X       DW  16E6h
    Y       DW  0D45Bh      ; D45B
    Z       DW  0C9C9h      ; C9C9

    ; Переменные для результатов
    X_prime DW  ?           ; Результат увеличения X
    Y_prime DW  ?           ; Результат увеличения Y
    Z_prime DW  ?           ; Результат увеличения Z
    M       DW  ?           ; Промежуточное значение M
    R       DW  ?           ; Конечный результат R

    ; Константы
    const_4341  DW  4341h
    const_66    DB  66h

.code
; -----------------------------------------------------------------
; Подпрограмма 1
; R = (M[8,10] = 1)
; Устанавливает биты 8 и 10 в 1, остальные в 0
SUBROUTINE1 PROC
    mov R, 0500h        ; 0500h = бит 8 (0100h) + бит 10 (0400h)
    ret
SUBROUTINE1 ENDP

; -----------------------------------------------------------------
; Подпрограмма 2
; R = M * 2
SUBROUTINE2 PROC
    mov ax, M           ; Загружаем M в ax
    shl ax, 1           ; Умножение на 2 (сдвиг влево)
    mov R, ax           ; Сохраняем результат
    ret
SUBROUTINE2 ENDP

; -----------------------------------------------------------------
; Адрес 1 (ADDR1)
; R = R | ~Z' (побитовое ИЛИ с инверсией Z')
ADDR1 PROC
    mov ax, Z_prime     ; Загружаем Z'
    not ax              ; Инвертируем Z' (побитовое НЕ)
    or ax, R            ; OR с R
    mov R, ax           ; Сохраняем результат
    ret
ADDR1 ENDP

; -----------------------------------------------------------------
; Адрес 2 (ADDR2)
; R = R + 66h
ADDR2 PROC
    mov ax, R           ; Загружаем R
    add ax, 66h         ; Прибавляем 66h
    mov R, ax           ; Сохраняем результат
    ret
ADDR2 ENDP

; -----------------------------------------------------------------
; Главная программа
Start:
    ; --- 1. В цикле увеличить на 3 старшие байты X, Y, Z ---
    ; Используем ECX как счетчик цикла (3 элемента)
    mov ecx, 3
    ; Адрес текущего элемента (начинаем с X)
    lea esi, X
    ; Адрес для сохранения результатов (начинаем с X_prime)
    lea edi, X_prime

cycle_increment:
    ; Загружаем текущее значение в ax
    mov ax, [esi]
    
    ; Увеличиваем старший байт (AH) на 3
    ; Младший байт (AL) остается без изменений
    add ah, 3
    
    ; Сохраняем результат по адресу в edi
    mov [edi], ax
    
    ; Переходим к следующему элементу (слова расположены подряд, +2 байта)
    add esi, 2
    add edi, 2
    
    ; Уменьшаем ecx и повторяем, пока ecx != 0
    loop cycle_increment

    ; --- 2. Вычислить M = X' - Y' + Z' ---
    ; Загружаем X' в ax
    mov ax, X_prime
    ; Вычитаем Y'
    sub ax, Y_prime
    ; Прибавляем Z'
    add ax, Z_prime
    ; Сохраняем результат в M
    mov M, ax

    ; --- 3. Проверка битов 2, 9, 10, 6 числа M (биты считаются от 0) ---
    ; Загружаем M в ax
    mov ax, M
    
    ; Проверка бита 2 (маска 0000 0100b = 4)
    test ax, 00000100b
    jnz sub2_jump          ; Если бит 2 != 0 -> подпрограмма 2
    
    ; Проверка бита 6 (маска 0100 0000b = 40h)
    test ax, 01000000b
    jnz sub2_jump          ; Если бит 6 != 0 -> подпрограмма 2
    
    ; Проверка бита 9 (маска 10 0000 0000b = 0200h)
    test ax, 0200h
    jnz sub2_jump          ; Если бит 9 != 0 -> подпрограмма 2
    
    ; Проверка бита 10 (маска 100 0000 0000b = 0400h)
    test ax, 0400h
    jnz sub2_jump          ; Если бит 10 != 0 -> подпрограмма 2
    
    ; Если мы здесь, значит все проверенные биты = 0
    ; Вызов подпрограммы 1
    call SUBROUTINE1
    jmp check_R_condition

sub2_jump:
    ; Вызов подпрограммы 2
    call SUBROUTINE2

check_R_condition:
    ; --- 4. Проверка R ---
    ; Сравниваем R с 4341h
    cmp R, 4341h
    je addr2_jump          ; Если R == 4341h, идем в АДР2
    
    ; АДР1: R = R | ~Z'
    call ADDR1
    jmp finish_program

addr2_jump:
    ; АДР2: R = R + 66h
    call ADDR2

finish_program:
    ; Завершение программы
    Invoke ExitProcess, 0

END Start