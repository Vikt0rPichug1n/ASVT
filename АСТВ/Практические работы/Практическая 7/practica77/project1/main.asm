$MOD52

ORG 0000h
LJMP START

ORG 0003h
LJMP INT0_ISR

ORG 000Bh
LJMP TIMER0_ISR

ORG 0013h
LJMP INT1_ISR

ORG 001Bh
LJMP TIMER1_ISR

ORG 100H

DUTY EQU 30H
HIGH_TIME_L EQU 31H
HIGH_TIME_H EQU 32H

PERIOD_H EQU 81H
PERIOD_L EQU 0CH

START:
MOV SP,#07H
MOV P1,#0FFH
MOV P3,#0FFH
CLR P1.0

MOV DUTY,#50

LCALL SET_HIGH_TIME

MOV TMOD,#11H
MOV TH1,#PERIOD_H
MOV TL1,#PERIOD_L

SETB IT0
SETB IT1
SETB EX0
SETB EX1
SETB ET1
SETB ET0
SETB EA
SETB TR1

MAIN:
SJMP MAIN

INT0_ISR:
MOV A,DUTY
CJNE A,#50,INC_DUTY
RETI

INC_DUTY:
ADD A,#10
MOV DUTY,A
LCALL SET_HIGH_TIME
RETI

INT1_ISR:
MOV A,DUTY
CJNE A,#20,DEC_DUTY
RETI

DEC_DUTY:
SUBB A,#10
MOV DUTY,A
LCALL SET_HIGH_TIME
RETI

SET_HIGH_TIME:
MOV A,DUTY
CJNE A,#20,C30
MOV HIGH_TIME_H,#19H
MOV HIGH_TIME_L,#64H
RET

C30:
CJNE A,#30,C40
MOV HIGH_TIME_H,#26H
MOV HIGH_TIME_L,#16H
RET

C40:
CJNE A,#40,C50
MOV HIGH_TIME_H,#32H
MOV HIGH_TIME_L,#0C8H
RET

C50:
CJNE A,#50,DEF
MOV HIGH_TIME_H,#3FH
MOV HIGH_TIME_L,#7AH
RET

DEF:
MOV HIGH_TIME_H,#3FH
MOV HIGH_TIME_L,#7AH
RET

TIMER1_ISR:
CLR TF1
MOV TH1,#PERIOD_H
MOV TL1,#PERIOD_L
SETB P1.0
CLR TR0
MOV A,#00H
SUBB A,HIGH_TIME_L
MOV TL0,A
MOV A,#00H
SUBB A,HIGH_TIME_H
MOV TH0,A
SETB TR0
RETI

TIMER0_ISR:
CLR TF0
CLR P1.0
CLR TR0
RETI

END